// Define MAX and MIN macros
#define MAX(X, Y) (((X) > (Y)) ? (X) : (Y))
#define MIN(X, Y) (((X) < (Y)) ? (X) : (Y))


enum Direction
{
    DIRECTION_STOP      = 0x0,
    DIRECTION_UP        = 0x1,
    DIRECTION_DOWN      = 0x2,
    DIRECTION_RIGHT     = 0x4,
    DIRECTION_LEFT      = 0x8
};
